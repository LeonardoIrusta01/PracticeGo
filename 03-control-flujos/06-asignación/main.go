package main

import "fmt"

func main() {
	a := 2
	// Operaciones de asignación

	/*  Suma en asignación */
	a += 2
	fmt.Println(a)

	/* Resta en asignación */
	a -= 2
	fmt.Println(a)

	/* Multiplicacion en asignación */
	a *= 4
	fmt.Println(a)

	/* División en asignación */
	a /= 2
	fmt.Println(a)

	/* Modulo de asignación */
	a %= 2
	fmt.Println(a)
}
